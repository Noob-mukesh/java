public class whileloop {
    public static void main(String[] args) {
        int count = 0;
        while (count < 100) {
            System.out.println("hello world");
            count++;

        }
        System.out.println("loop ended");
        while (true) {
            System.out.println("infinite loop");

        }
        // System.out.println("not going to print");

    }
}
